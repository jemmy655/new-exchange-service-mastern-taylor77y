    public static HttpResponse doGet(String host, String path, String method,
                                     Map<String, String> headers,
                                     Map<String, String> querys)
            throws Exception {
        HttpClient httpClient = wrapClient(host);

        HttpGet request = new HttpGet(buildUrl(host, path, querys));
        for (Map.Entry<String, String> e : headers.entrySet()) {
            request.addHeader(e.getKey(), e.getValue());
        }

        return httpClient.execute(request);
    }

    /**
     * get
     *
     * @param host
     * @param path
     * @param method
     * @param headers
     * @param querys
     * @return
     * @throws Exception
     */
    public static String doGet(String host, String apiKey) throws Exception {
        String result = "";
        CloseableHttpClient httpClient = HttpClients.createDefault();
        HttpGet httpget = new HttpGet(host);
        // 创建HttpClientBuilder设置属性
        HttpClientBuilder httpClientBuilder = HttpClientBuilder
                .create().setDefaultRequestConfig(RequestConfig.custom().setConnectionRequestTimeout(6000)
                        .setSocketTimeout(6000).setConnectTimeout(6000).build())
                .setRetryHandler(new DefaultHttpRequestRetryHandler(3, true));
        // 设置请求头信息
        Map<String, String> map = new HashMap<>();
        map.put("Content-Type", "application/json");
        map.put("X-API-Key", apiKey);
        for (Map.Entry<String, String> entry : map.entrySet()) {
            httpget.setHeader(entry.getKey(), entry.getValue());
        }
        // 创建HttpClient对象，CloseableHttpClient实例的生成器
        httpClient = httpClientBuilder.build();
        try {
            // 发送HttpPost请求，获取返回值
            CloseableHttpResponse response = httpClient.execute(httpget);
            result = EntityUtils.toString(response.getEntity(), Consts.UTF_8);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                // 释放资源
                httpClient.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        return result;
    }

    /**
     * post form
     *
     * @param host
     * @param path
     * @param method
     * @param headers
     * @param querys
     * @param bodys
     * @return
     * @throws Exception
     */
    public static HttpResponse doPost(String host, String path, String method, Map<String, String> headers,
                                      Map<String, String> querys, Map<String, String> bodys) throws Exception {
        HttpClient httpClient = wrapClient(host);

        HttpPost request = new HttpPost(buildUrl(host, path, querys));
        for (Map.Entry<String, String> e : headers.entrySet()) {
            request.addHeader(e.getKey(), e.getValue());
        }

        if (bodys != null) {
            List<NameValuePair> nameValuePairList = new ArrayList<NameValuePair>();

            for (String key : bodys.keySet()) {
                nameValuePairList.add(new BasicNameValuePair(key, bodys.get(key)));
            }
            UrlEncodedFormEntity formEntity = new UrlEncodedFormEntity(nameValuePairList, "utf-8");
            formEntity.setContentType("application/x-www-form-urlencoded; charset=UTF-8");
            request.setEntity(formEntity);
        }

        return httpClient.execute(request);
    }

